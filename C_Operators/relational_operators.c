#include<stdio.h>
void main()
{
    int number1=10,number2=20;
    printf("%d == %d : %d\n",number1,number2,number1==number2);
    printf("%d != %d : %d\n",number1,number2,number1!=number2);
    printf("%d > %d : %d\n",number1,number2,number1>number2);
    printf("%d < %d : %d\n",number1,number2,number1<number2);
    printf("%d >= %d : %d\n",number1,number2,number1>=number2);
    printf("%d <= %d : %d\n",number1,number2,number1<=number2);
}